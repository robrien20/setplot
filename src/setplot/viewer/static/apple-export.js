// Apple Music playlist export — runs entirely in the browser via MusicKit JS.
// The backend's only job is signing the developer JWT. User auth (Apple ID
// sign-in) and library writes happen here.
//
// Flow:
//   1. Fetch the developer token from /api/apple/dev-token.
//   2. Configure MusicKit, prompt the user to authorize() on first export.
//   3. Fetch the recognized track list from /api/export/tracks/<set_id>.
//   4. For each track: resolve to an Apple Music catalog song id
//        — direct apple_id   → use as-is
//        — ISRC              → catalog/{store}/songs?filter[isrc]=<isrc>
//        — title+artist      → catalog/{store}/search?term=...&types=songs
//   5. POST /v1/me/library/playlists with the resolved IDs as relationships.
//
// Returns a summary similar to the Spotify path: {playlist_url, matched, unmatched}.

const MUSICKIT_VERSION = "3";
let musicReady = null;

async function ensureMusicKitLoaded() {
  if (window.MusicKit && window.MusicKit.getInstance) return;
  await new Promise((resolve, reject) => {
    document.addEventListener("musickitloaded", () => resolve(), { once: true });
    const s = document.createElement("script");
    s.src = `https://js-cdn.music.apple.com/musickit/v${MUSICKIT_VERSION}/musickit.js`;
    s.async = true;
    s.onerror = () => reject(new Error("failed to load MusicKit JS"));
    document.head.appendChild(s);
  });
}

async function getMusic() {
  if (musicReady) return musicReady;
  musicReady = (async () => {
    await ensureMusicKitLoaded();
    const r = await fetch("/api/apple/dev-token");
    if (!r.ok) throw new Error("apple integration not configured server-side");
    const { token } = await r.json();
    await window.MusicKit.configure({
      developerToken: token,
      app: { name: "SetPlot", build: "1.0" },
    });
    return window.MusicKit.getInstance();
  })();
  return musicReady;
}

function isApiError(err, code) {
  return err && err.errors && err.errors.some(e => String(e.status) === String(code));
}

async function resolveOne(music, tk) {
  if (tk.apple) return { id: tk.apple, reason: "acr_id" };

  const store = music.storefrontId || "us";
  if (tk.isrc) {
    try {
      const r = await music.api.music(
        `/v1/catalog/${store}/songs`,
        { "filter[isrc]": tk.isrc, limit: 1 }
      );
      const items = (r.data && r.data.data) || [];
      if (items.length) return { id: items[0].id, reason: "isrc" };
    } catch (_) { /* fall through to text search */ }
  }

  if (tk.title && tk.artists) {
    try {
      const r = await music.api.music(
        `/v1/catalog/${store}/search`,
        { term: `${tk.title} ${tk.artists}`, types: "songs", limit: 5 }
      );
      const items = ((r.data && r.data.results && r.data.results.songs) || {}).data || [];
      const wantArtist = tk.artists.toLowerCase();
      // Prefer hits whose artistName overlaps the recognized artist.
      const safe = items.find(it => {
        const a = ((it.attributes || {}).artistName || "").toLowerCase();
        return wantArtist.split(",").some(piece => piece.trim() && a.includes(piece.trim()));
      });
      if (safe) return { id: safe.id, reason: "search" };
      if (items.length) return { id: items[0].id, reason: "search" };
    } catch (_) {}
  }
  return { id: null, reason: "unmatched" };
}

export async function exportToAppleMusic(setId) {
  const music = await getMusic();
  if (!music.isAuthorized) {
    await music.authorize();  // prompts sign-in on first call
  }

  const r = await fetch(`/api/export/tracks/${encodeURIComponent(setId)}`);
  if (!r.ok) throw new Error(`could not fetch tracks: ${r.status}`);
  const { set_title, tracks } = await r.json();
  if (!tracks.length) throw new Error("nothing to export");

  const matched = [];
  const unmatched = [];
  const seen = new Set();
  const breakdown = { acr_id: 0, isrc: 0, search: 0, unmatched: 0 };
  for (const tk of tracks) {
    const { id, reason } = await resolveOne(music, tk);
    breakdown[reason] = (breakdown[reason] || 0) + 1;
    if (id && !seen.has(id)) {
      matched.push({ id, type: "songs" });
      seen.add(id);
    } else if (!id) {
      unmatched.push({ title: tk.title, artists: tk.artists, isrc: tk.isrc });
    }
  }
  if (!matched.length) throw new Error("couldn't resolve any tracks on Apple Music");

  const today = new Date().toISOString().slice(0, 10);
  const body = {
    attributes: {
      name: `${set_title} — recognized tracks`,
      description: `Generated by SetPlot on ${today}`,
    },
    relationships: { tracks: { data: matched } },
  };
  const result = await music.api.music(
    "/v1/me/library/playlists",
    {},
    { fetchOptions: { method: "POST", body: JSON.stringify(body) } }
  );
  const created = (result.data && result.data.data && result.data.data[0]) || {};
  const libraryId = created.id || "";
  const playlistUrl = libraryId
    ? `https://music.apple.com/library/playlist/${libraryId}`
    : "";
  return {
    playlist_url: playlistUrl,
    matched: matched.length,
    unmatched,
    resolution: breakdown,
  };
}
